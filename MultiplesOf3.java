public class MultiplesOf3
{
    public static void main(String[] args)
    {
        System.out.println("Multiples of 3");
        System.out.println();
        //print multiples of 3
        for (int i=0; i<10; i++)
        {
            if (i%3==0)
            System.out.println(i);
        }
    }
}